#include "libft.h"

int		ft_atoi(const char *str)
{
	int				sign;
	long long		res;

	sign = 1;
	res = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
	{
		str++;
	}
	if (*str == '+' || *str == '-')
	{
		sign = ((*str == '-') ? -1 : 1);
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = 10 * res + (*str - '0');
		str++;
	}
	return ((res * sign));
}
